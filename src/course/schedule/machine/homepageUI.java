    public HashSet<HopkinsCourse> merge()
    {
         HashSet<HopkinsCourse> results = getTakenCourses();
         HashSet<HopkinsCourse> other = getAPIB();
             
         for (HopkinsCourse a : other)
         {
             if (!results.contains(a))
                 results.add(a);
         }
        
        return results;
    }
    
