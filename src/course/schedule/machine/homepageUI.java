    public String major()
    {
        String myMajor = major.getSelectedItem().toString().trim();
        if (myMajor.equals("Please select one"))
        {
           
        }
        else if (myMajor.equals("Biomedical Engineering"))
            return "majors/bme";
         else if (myMajor.equals("Molecular and Cellular Biology"))
            return "majors/molcelbio";
         else if (myMajor.equals("Materials Science Engineering"))
            return "majors/mse";
        else if (myMajor.equals("Biophysics"))
            return "majors/biophysics";
        return "Error";
    }
    
    
    public String minor()
    {
        String myMinor = minor.getSelectedItem().toString().trim();
        if (myMinor.equals("N/A"))
        {
           return "N/A";
        }
        else if (myMinor.equals("Math"))
            return "minors/math";
         else if (myMinor.equals("Computer Science Analysis"))
            return "minors/cs analysis";
         else if (myMinor.equals("Computer Science Systems"))
            return "minors/cs systems";
        else if (myMinor.equals("Computer Science Applications"))
            return "minors/cs applications";
        return "Error";
    }
    
    public ArrayList<String> category()
    {
        ArrayList<String> results = new ArrayList<String>();
        if (major().equals("Error"))
        {
            errorMessageMajor.setText("Error. Please select a Major");
            return null;
        }
        else
        {
             errorMessageMajor.setText("");
             results.add(major());
             if (!minor().equals("N/A"))
             {
                 results.add(minor());
             }
              if (premed.isSelected())
            {
              results.add("premed");
                 }
        }
       
        return results;
    }
    
    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        // TODO add your handling code here:
        addAPIB();
        ArrayList<String> categories = category();
        if (categories == null) return;
          HashSet<HopkinsCourse> coursesTaken = merge();
        String one, two, three;
        one = "";
        two = "";
        three = "";
        one = categories.get(0);
        if (categories.size() > 1)
        two = categories.get(1);
        
         if (categories.size() > 2)
        three = categories.get(2);
        ArrayList<HopkinsCourse> priorityCourses = new ArrayList<HopkinsCourse>();
        if (one.length() != 0)
        {
            if (two.length() != 0 && three.length() != 0)
                priorityCourses = CourseListGenerator.generateBestSchedule(coursesTaken,categories);
            else if (two.length() == 0 && three.length() != 0)
            {
                priorityCourses = CourseListGenerator.generateBestSchedule(coursesTaken,categories);
            }
            
            else if (two.length() != 0 && three.length() == 0)
            {
                priorityCourses = CourseListGenerator.generateBestSchedule(coursesTaken,categories);
            }
            else if (two.length() == 0 && three.length() == 0)
            {
                priorityCourses = CourseListGenerator.generateBestSchedule(coursesTaken,categories);
            }
        }
      double desiredCredits = Double.parseDouble(credit.getSelectedItem().toString());
      String stringSemester = term.getSelectedItem().toString().toUpperCase();
      HopkinsClass.Semester currentSemester = HopkinsClass.Semester.valueOf(stringSemester.split(" ",2)[0]);
      String toSet = "Top priority courses: " + priorityCourses.toString().replace("[", "").replace("]", "");
      if (toSet.length()>143) toSet = toSet.substring(0,140) + "...";
      generatedImportantCourses.setText(toSet);
       ArrayList<HopkinsClass> finalSchedule = Schedule.matchTimes(priorityCourses, desiredCredits, currentSemester);
       String toPut = "";
       String newLn = ManageTxtFiles.newLn;
       for (HopkinsClass hc : finalSchedule) {
           toPut = toPut + HopkinsCourse.getCourse(hc.courseTitle).verbalName + " " + hc.courseTitle + " (" + hc.getSection() + ") " + hc + newLn;
       }
       scheduleDisplayer.setText(toPut);
    }//GEN-LAST:event_submitActionPerformed

    
    
